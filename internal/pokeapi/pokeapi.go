func GetLocationArea(config *Config, forward bool) ([]string, error) {
	var url string
	if forward {
		if config.NextUrl == "" {
			return nil, fmt.Errorf("you're on the last page")
		}
		url = config.NextUrl
	} else {
		if config.PrevUrl == "" {
			return nil, fmt.Errorf("you're on the first page")
		}
		url = config.PrevUrl
	}
	res, err := http.Get(url)
	if err != nil {
		return nil, err
	}
	defer res.Body.Close()

	var locations LocationList
	decoder := json.NewDecoder(res.Body)
	if err = decoder.Decode(&locations); err != nil {
		return nil, err
	}

	if forward {
		config.PrevUrl = config.NextUrl
		if locations.Next == nil {
			config.NextUrl = ""
		}
		config.NextUrl = *locations.Next
	} else {
		config.NextUrl = config.PrevUrl
		if locations.Previous == nil {
			config.PrevUrl = ""
		}
		config.PrevUrl = *locations.Next
	}

	var areas []string
	for _, area := range locations.Results {
		areas = append(areas, area.Name)
	}
	return areas, nil
}

func GetLocalPokemon(locationUrl string) ([]string, error) {
	res, err := http.Get(locationUrl)
	if err != nil {
		return nil, err
	}
	defer res.Body.Close()

	decoder := json.NewDecoder(res.Body)
	var payload Location
	err = decoder.Decode(&payload)
	if err != nil {
		return nil, err
	}

	var pokemon []string
	for _, encounter := range payload.PokemonEncounters {
		pokemon = append(pokemon, encounter.Pokemon.Name)
	}
	return pokemon, nil
}

func GetPokemon(pokemonUrl string) (Pokemon, error) {
	res, err := http.Get(pokemonUrl)
	if err != nil {
		return Pokemon{}, err
	}
	defer res.Body.Close()

	decoder := json.NewDecoder(res.Body)
	var pokemon Pokemon
	if err = decoder.Decode(&pokemon); err != nil {
		return Pokemon{}, err
	}
	return pokemon, nil
}

func (p Pokedex) Catch(pokemon Pokemon) bool {
	baseExp := pokemon.BaseExperience
	/*
		36 is the lowest base exp, and the chance to catch should be 1.
		for any other Pokemon, the chance would be 36 / base exp.
		we multiply chance by 100, and roll a 100-sided die against it.
		if the roll is below the chance, it is caught.
	*/
	chance := int32(math.Round(float64(3600) / float64(baseExp)))
	roll := rand.Int32N(101)
	if roll <= chance {
		p[pokemon.Name] = pokemon
		return true
	}
	return false
}

func (p Pokedex) Has(pokemonName string) (Pokemon, bool) {
	if pokemon, ok := p[pokemonName]; ok {
		return pokemon, true
	}
	return Pokemon{}, false
}

func (p Pokedex) GetAll() []string {
	var names []string
	for n := range p {
		names = append(names, n)
	}
	return names
}

func GetStats(pokemon Pokemon) Pokestats {
	var types []string
	for _, typeEntry := range pokemon.Types {
		types = append(types, typeEntry.Type.Name)
	}
	var stats []struct {
		Name string
		Val  int
	}
	for _, statEntry := range pokemon.Stats {
		name := statEntry.Stat.Name
		val := statEntry.BaseStat
		stats = append(stats, struct {
			Name string
			Val  int
		}{Name: name, Val: val})
	}

	return Pokestats{
		Name:   pokemon.Name,
		Height: pokemon.Height,
		Weight: pokemon.Weight,
		Stats:  stats,
		Types:  types,
	}
}
